package pets;

public class ExoticAnimal {
	String id;
	String name;
	String type;
	String species;
	int age;
	
	public ExoticAnimal(String id, String name, String type, String species, int age) {
		this.id = id;
		this.name = name;
		this.type = type;
		this.species = species;
		this.age = age;
	}
	
}
